// heart
const heartcount= document.getElementById('heart-count');


document.getElementById('heart').addEventListener('click',function(e){
    e.preventDefault();

    
    document.getElementById('heart').style.backgroundColor = 'gray';
    document.getElementById('heart').style.borderRadius ='50%';

    let count = parseInt(heartcount.innerText);
    count++;
    heartcount.innerText = count;
    
})



